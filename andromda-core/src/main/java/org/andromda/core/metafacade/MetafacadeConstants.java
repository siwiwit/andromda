package org.andromda.core.metafacade;


/**
 * Contains constants for use with metafacades.
 * For example, the metafacade namespace separator <em>::</em>
 * is contained within this class.
 *
 * @author Chad Brandon
 */
public interface MetafacadeConstants
{
    /**
     * Used to separate the metafacade namespaces.
     */
    public static final String NAMESPACE_SCOPE_OPERATOR = "::";
    
    /**
     * The suffix for the metafacade implementation classes (each metafacade
     * implementation class ends with this string).
     */
    public static final String METAFACADE_IMPLEMENTATION_SUFFIX = "Impl";
}